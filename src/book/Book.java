package book;

public class Book {
	private String name;
	private String address;
	private String city;
	private String state;
	private String zip;
	private String phone;
	private String email;

	public String getName() {

		return name;
	}
	public void setName(String val1){
		name = val1;
	}
	public String getAddress() {

		return address;
	}
	public void setAddress(String val2){
		address = val2;
	}
	public String getCity() {

		return city;
	}
	public void setCity(String val3){
		city = val3;
	}
	public String getState() {

		return state;
	}
	public void setState(String val4){
		state = val4;
	}
	public String getZip() {

		return zip;
	}
	public void setZip(String val5){
		zip = val5;
	}
	public String getPhone() {

		return phone;
	}
	public void setPhone(String val6){
		phone = val6;	
	}
	public String getEmail() {

		return email;
	}
	public void setEmail(String val7){
		email = val7;	
	}
}